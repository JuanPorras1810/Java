//Metodos 
    public void CrearTabla() {
        Object modelodata[][] = new Object[0][0];
        Object modelotitulos[] = {"Fecha", "Asesor", "Total interacciones", "Cerrado", "Abierto", "Escalado", "Tiempo total", "Promedio tiempo"};
        tabladatos = new DefaultTableModel();
        tabladatos = new DefaultTableModel(modelodata, modelotitulos);
        this.TblGes.setModel(this.tabladatos);
    }

    public void BorrarTabla() {
        while (0 < this.TblGes.getRowCount()) {
            TblGes.setModel(new DefaultTableModel());
            this.CrearTabla();
        }
    }

    public void LlenarTabla() {
        try {
            this.BorrarTabla();
            objClsIndicadoresGestion.LlenarTabla();
            while (objClsIndicadoresGestion.DatosTabla.next() == true) {
                String fecha = objClsIndicadoresGestion.DatosTabla.getString(1);
                String asesor = objClsIndicadoresGestion.DatosTabla.getString(2);
                String TotalInteraccion = objClsIndicadoresGestion.DatosTabla.getString(3);
                String cerrado = objClsIndicadoresGestion.DatosTabla.getString(4);
                String abierto = objClsIndicadoresGestion.DatosTabla.getString(5);
                String escalado = objClsIndicadoresGestion.DatosTabla.getString(6);
                String TiempoTotal = objClsIndicadoresGestion.DatosTabla.getString(7);
                String PromedioTiempo = objClsIndicadoresGestion.DatosTabla.getString(8);
                Object fila[] = {fecha, asesor, TotalInteraccion, cerrado, abierto, escalado, TiempoTotal, PromedioTiempo};
                tabladatos.addRow(fila);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al llenar la tabla: " + e);
        }
    }

    public void regresar() {
        FrmIndex objFrmIdex = new FrmIndex();
        objFrmIdex.setVisible(true);
        this.dispose();
    }

